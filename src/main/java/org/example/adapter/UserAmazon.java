package org.example.adapter;

public class UserAmazon {
    private String idAmazon;
    private String username;
    private String amazonEmail;
    private String lastName;

    public String getIdAmazon() {
        return idAmazon;
    }

    public void setIdAmazon(String idAmazon) {
        this.idAmazon = idAmazon;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getAmazonEmail() {
        return amazonEmail;
    }

    public void setAmazonEmail(String amazonEmail) {
        this.amazonEmail = amazonEmail;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
}
